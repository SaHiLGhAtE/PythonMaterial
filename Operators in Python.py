#comparison operators
print("comparison operators")
i=5
print(i==8)
print(i!=8)

#logical operators
print("logical operators")
a=True
b=False
print(a and b)
print(a and a)
print(a or b)
print(b or b)

#identity operators
print("identity operators")
a=5
b=7
print(a is b)
